#include <iostream>

using namespace std;

int main() {
    int a = 13;
    int b = 4;

    cout << (a == b) << endl; // 0
    cout << (a != b) << endl; // 1
    cout << (a > b) << endl; // 1
    cout << (a < b) << endl; // 0
    cout << (a >= b) << endl; // 1
    cout << (a <= b) << endl; // 0

    return 0;

}